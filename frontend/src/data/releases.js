export const getReleaseTotalTracks = () => {
  return releases.reduce((total, release) => total + release.tracks.length, 0)
}

export const getReleaseById = (id) => {
  return releases.find((release) => release.id === id)
}

export const getLatestReleases = (count = 5) => {
  return releases
    .sort((a, b) => new Date(b.releaseDate) - new Date(a.releaseDate))
    .slice(0, count)
}

export const calculateRoyaltyShare = (
  streamCount,
  earningsPerStream = 0.004,
) => {
  const totalEarnings = streamCount * earningsPerStream
  const artistShare = totalEarnings * 0.7 // 70% to artist
  const stakersShare = totalEarnings * 0.3 // 30% to stakers

  return {
    totalEarnings,
    artistShare,
    stakersShare,
  }
}
